package edu.daniel.silva.Calculadora.process;

/**
 * Clase que proporciona la operación básica de resta.
 */
public class Resta {
    /**
     * Realiza la resta de dos números enteros.
     * @param a Minuendo.
     * @param b Sustraendo.
     * @return El resultado de la resta a - b.
     */
    public static int realizarOperacion(int a, int b) {
        return a - b;
    }
}