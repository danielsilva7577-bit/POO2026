package edu.daniel.silva.Calculadora.process;

/**
 * Clase que proporciona la operación básica de suma.
 */
public class Suma {
    /**
     * Realiza la suma de dos números enteros.
     * @param a Primer sumando.
     * @param b Segundo sumando.
     * @return El resultado de la suma a + b.
     */
    public static int realizarOperacion(int a, int b) {
        return a + b;
    }
}